package HomeworkLoops;

public class Homework01 {

	public static void main(String[] args) {
		// Print numbers from 1 to 100 in 1 line with space
		
		int i=1;
		
		while(i<=100) {
			System.out.print(i+" ");
			i++;
		}
		System.out.println("---------------------------------------------------------");
		
		int ii=100;
		
		while(ii>=0) {
			System.out.print(ii+" ");
			ii--;
		}

	}

}
