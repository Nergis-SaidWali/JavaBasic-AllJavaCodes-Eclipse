package com.syntax.class07;

public class ForLoopDemo {

	public static void main(String[] args) {
	
		for(int i=1; i<=5; i++) {
			System.out.println("Hello");
		}
		
		System.out.println("---------------------------------------------------------------");
		//print numbers from 1 to 20
		for(int i=1; i<=20; i++) {
			System.out.print(i+" ");
		}

	}

}
